// abs makes neg number to positive
var num = -5;
var absolute = Math.abs(num);
console.log(absolute);

//Round closest num high low 
var num1 = 50.05;
var round = Math.round(num1);
console.log(round);

//ceil always makes high number
var num2 = 50.55;
var ceil = Math.ceil(num2);
console.log(ceil);

//floor always makes low number
var num3 = 50.55;
var round3 = Math.floor(num3);
console.log(round3);

//random any number
var roll = 100;
var random = Math.random(roll) * 100;
var round2 = Math.round(random);
console.log(round2); 

